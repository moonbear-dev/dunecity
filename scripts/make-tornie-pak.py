def main():
    blobs = []
    paken_names = []
    for name in ENTRIES:
        pak_name, path = resolve_entry(name)
        if path is None:
            print(f"ERROR: missing {name} (looked in data/ and mods/Tornie/campaign/)", file=sys.stderr)
            sys.exit(1)
        blobs.append(open(path, "rb").read())
        paken_names.append(pak_name)

    # header size: per entry 4 bytes offset + len(name)+1 null, plus 4 byte terminator
    header_size = sum(4 + len(n) + 1 for n in paken_names) + 4

    # compute absolute offsets
    data_offset = 0
    offsets = []
    for blob in blobs:
        offsets.append(header_size + data_offset)
        data_offset += len(blob)

    with open(OUT, "wb") as f:
        # PAK entry names are paken_names (bare filenames like 'scenr001.ini',
        # even when the source file lives in mods/Tornie/campaign/). Engine
        # code calls pFileManager with bare filenames, so writing
        # 'campaign/scenr001.ini' to the PAK header would defeat lookup.
        for name, off in zip(paken_names, offsets):
            f.write(struct.pack("<I", off))
            f.write(name.encode("ascii") + b"\x00")
        f.write(struct.pack("<I", 0))  # terminator
        for blob in blobs:
            f.write(blob)

    print(f"Wrote {os.path.getsize(OUT)} bytes to {OUT}")
    print(f"  {len(ENTRIES)} entries packed")

if __name__ == "__main__":
    main()
