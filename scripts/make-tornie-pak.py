#!/usr/bin/env python3
"""Build data/Tornie.PAK from loose PNG files in data/.

PAK format (Dune Legacy / Westwood):
  Header:  [uint32_le offset, null-terminated name] × N, then uint32_le 0
  Body:    raw file data concatenated in entry order
Offsets are absolute byte positions in the file.
"""

import struct, os, sys
from pathlib import Path

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
DATA = Path(REPO) / "data"
MODS_TORNIE = Path(REPO) / "mods" / "Tornie"
OUT  = DATA / "Tornie.PAK"

# Some assets live in mods/Tornie/campaign/ rather than data/
# (the Rebels campaign scenarios ship with the mod on disk; data/
# holds engine-visible artifacts only). Resolve per entry below.
def resolve_entry(name):
    """Return (pak_entry_name, on_disk_path) for a given ENTRIES list entry.
    The on-disk path may live in either data/ (default) or in a subdir
    of mods/Tornie/ (e.g. mod-side Rebels scenarios). The pak_entry_name
    is what gets written into the PAK header — engine code calls pFileManager
    with bare filenames like 'scenr001.ini', so we must strip the
    'campaign/' source-path prefix and emit just the basename."""
    parts = name.split("/", 1)
    if parts[0] == "campaign":
        rel = parts[1] if len(parts) > 1 else ""
        base = MODS_TORNIE / "campaign"
        pak_name = rel                 # strip 'campaign/' prefix
    else:
        rel = name
        base = DATA
        pak_name = name
    direct = base / rel
    if direct.is_file():
        return pak_name, direct
    # case-insensitive fallback (Linux ext4 is case-sensitive; macOS/Windows are not)
    if base.is_dir():
        for candidate in base.iterdir():
            if candidate.name.lower() == rel.lower() and candidate.is_file():
                return pak_name, candidate
    return pak_name, None

